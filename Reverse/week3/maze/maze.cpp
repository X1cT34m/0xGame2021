int check(char * route)
{
    int length;
    char t;
    length = strlen(route);
    for(int i = 0;; ++i)
    {
        if(maze[24 * x + y] == 'E')
            return 1;
        if(maze[24 * x + y] != 'S' && maze[24 * x + y] != '.')
            return 0;
        t = route[i];
        switch(t)
        {
            case 'w':
                --x;
                break;
            case 's':
                ++x;
                break;
            case 'a':
                --y;
                break;
            case 'd':
                ++y;
                break;
            default:
                return 0;
        }
        if(length > 71)
            return 0;
    }
    return 0;
}


